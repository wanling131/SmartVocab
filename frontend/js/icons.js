/**
 * Get SVG icon HTML string
 * @param {string} name - Icon name from ICONS map
 * @param {object} options - { size: 'sm'|'lg'|'xl', color: 'klein'|'muted'|'coral'|'white'|'amber'|'sage', className: '' }
 * @returns {string} HTML string
 */
export function getIcon(name, options = {}) {
    const svg = ICONS[name];
    if (!svg) {
        console.warn(`Icon "${name}" not found`);
        return '';
    }

    const classes = ['icon'];
    if (options.size) classes.push(`icon-${options.size}`);
    if (options.color) classes.push(`icon-${options.color}`);
    if (options.className) classes.push(options.className);

    return `<span class="${classes.join(' ')}">${svg}</span>`;
}

